import Foundation

/**
 Defines the position that the Tab Bar is drawn in.
 */
public enum MultiplatformTabViewPosition {
    /// Place Tabs at the top of the screen.
    case top
    
    /// Place Tabs at the bottom of the screen.
    case bottom
    
    /// Place Tabs on the left side of the screen.
    case left
    
    /// Place tabs on the right side of the screen.
    case right
}
