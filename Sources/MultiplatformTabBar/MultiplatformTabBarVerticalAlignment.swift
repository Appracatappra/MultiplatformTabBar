import Foundation

/**
 Defines the Vertical Alignment for a `MultiplatformTabBar`.
 */
public enum MultiplatformTabBarVerticalAlignment {
    /// Aligns to the top.
    case top
    
    /// Aligns to the center.
    case center
    
    /// Aligns to the bottom.
    case bottom
}
